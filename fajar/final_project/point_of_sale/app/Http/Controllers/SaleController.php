     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $sale = new Sale();
        $sale->member_id = null;
        $sale->total_items = 0;
        $sale->total_price = 0;
        $sale->discount = 0;
        $sale->paid = 0;
        $sale->received = 0;
        $sale->user_id = auth()->id();
        $sale->save();


        session(['sales_id' => $sale->id]);

        return redirect()->route('transaction.index');
    }

